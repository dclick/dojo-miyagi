package br.com.dclick.dojo.conceitos;


public class Range {

	double x;
	double y;
	String nota;

	public Range(double x, double y, String nota) {
		super();
		this.x = x;
		this.y = y;
		this.nota = nota;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public String getNota() {
		return nota;
	}

	public void setNota(String nota) {
		this.nota = nota;
	}
	
	

}
